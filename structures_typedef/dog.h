#ifndef DOG_H
#define DOG_H

/**
 * struct dog - Struct that defines a dog with a name, age, and owner.
 * @name: Name of the dog, type = char *
 * @age: Age of the dog, type = float
 * @owner: Owner of the dog, type = char *
 */
struct dog
{
	char *name;
	float age;
	char *owner;
};

#endif /* DOG_H */

